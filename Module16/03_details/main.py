shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]

detal = input('Название детали: ')
summa = 0
count = 0

for i in range(len(shop)):
        if shop[i][0] == detal:
                count += 1
                summa += shop[i][1]
print('Кол-во деталей — ',count,'\nОбщая стоимость — ',summa)
